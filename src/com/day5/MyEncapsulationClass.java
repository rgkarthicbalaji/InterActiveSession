package com.day5;

//Normal class is example for encapsulation
public class MyEncapsulationClass {
    int myInt = 0;
    String myString = "";

    public int getMyInt() {
        return myInt;
    }

    public void setMyInt(int myInt) {
        this.myInt = myInt;
    }

    public String getMyString() {
        return myString;
    }

    public void setMyString(String myString) {
        this.myString = myString;
    }
}
